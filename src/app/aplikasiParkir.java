    private void submitKeluarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitKeluarButtonActionPerformed
        // TODO add your handling code here:
        if(noParkirKeluarTextField.getText().equals("") || 
                jenisKendaraanKeluarButtonGroup.getSelection() == null ||
                platNomorKeluarTextField.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Data Belum Lengkap", "Error", JOptionPane.ERROR_MESSAGE);
        } else if(lamaParkirTextField.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Silakan Masukkan Lama Parkir!", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            float biaya;
            float lamaParkir = Float.valueOf(lamaParkirTextField.getText());
            if(jenisKendaraanKeluarButtonGroup.getSelection().getActionCommand().equals("Motor")) {
                biaya = 2000*lamaParkir;
                biayaTextField.setText(String.format("Rp%.02f", biaya));
            } else if(jenisKendaraanKeluarButtonGroup.getSelection().getActionCommand().equals("Mobil")) {
                biaya = 5000*lamaParkir;
                biayaTextField.setText(String.format("Rp%.02f", biaya));
            }
            parkirKeluarTextArea.append("\n" + noParkirKeluarTextField.getText() + " | " +
                    jenisKendaraanKeluarButtonGroup.getSelection().getActionCommand() + " | " +
                    platNomorKeluarTextField.getText() + " | " +
                    lamaParkirTextField.getText() +  " Jam | " +
                    biayaTextField.getText());
        }
    }//GEN-LAST:event_submitKeluarButtonActionPerformed

