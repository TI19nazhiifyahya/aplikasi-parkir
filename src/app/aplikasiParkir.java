    private void cariNoParkirKeluarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cariNoParkirKeluarButtonActionPerformed
        // TODO add your handling code here:
        if(noParkirKeluarTextField.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Silakan Masukkan No Parkir!", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            boolean ketemu = false;
            for(String[] i: kendaraan) {
                if(i[0].equals(noParkirKeluarTextField.getText())) {
                    if(i[1].equals("Motor")) {
                        jenisKendaraanKeluarButtonGroup.setSelected(motorKeluarRadioButton.getModel(), true);
                        platNomorKeluarTextField.setText(i[2]);
                        ketemu = true;
                        break;
                    } else if(i[1].equals("Mobil")) {
                        jenisKendaraanKeluarButtonGroup.setSelected(mobilKeluarRadioButton.getModel(), true);
                        platNomorKeluarTextField.setText(i[2]);
                        ketemu = true;
                        break;
                    }
                }
            }
            if(ketemu == false) {
                JOptionPane.showMessageDialog(this, "Data Tidak Ditemukan", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_cariNoParkirKeluarButtonActionPerformed

